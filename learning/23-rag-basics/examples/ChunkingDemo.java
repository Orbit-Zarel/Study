public class ChunkingDemo {

    public static void main(String[] args) {
        System.out.println("原始文档：一篇很长的财报");
        System.out.println("切块后：");
        System.out.println("chunk-1 -> 公司概况");
        System.out.println("chunk-2 -> 收入与利润");
        System.out.println("chunk-3 -> 风险提示");
    }
}
